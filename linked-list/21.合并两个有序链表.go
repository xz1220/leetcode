/**
 * Definition for singly-linked list.
 * type ListNode struct {
 *     Val int
 *     Next *ListNode
 * }
 */
 func mergeTwoLists(l1 *ListNode, l2 *ListNode) *ListNode {
    if l1 == nil {
        return l2
    }
    if l2 == nil {
        return l1
    }

    dummyNode := &ListNode{0, l2}
    pre := dummyNode
    for l1 != nil {
        if l2 == nil {
            pre.Next = l1
            break 
        }

        if l1.Val < l2.Val {
            next := l1.Next
            l1.Next = l2
            pre.Next = l1
            l1 = next
            pre = pre.Next
        }else {
            l2 = l2.Next
            pre = pre.Next
        }
    }
    
    return dummyNode.Next
}
